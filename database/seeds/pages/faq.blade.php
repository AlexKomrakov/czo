                    <p>
                        Межевой план представляет собой документ, который составлен на основе кадастрового плана соответствующей территории или кадастровой выписки о соответствующем земельном участке и в котором воспроизведены определенные внесенные в государственный кадастр недвижимости сведения и указаны сведения об образуемых земельном участке или земельных участках, либо о части или частях земельного участка, либо новые необходимые для внесения в государственный кадастр недвижимости сведения о земельном участке или земельных участках (ФЗ от 24.07.2007г. «О государственном кадастре недвижимости статья 38).
                    </p>
