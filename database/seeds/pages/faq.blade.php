                        Технический план представляет собой документ, в котором воспроизведены определенные сведения, внесенные в государственный кадастр недвижимости, и указаны сведения о здании, сооружении, помещении или об объекте незавершенного строительства, необходимые для постановки на учет такого объекта недвижимости, либо сведения о части или частях такого объекта недвижимости, либо новые необходимые для внесения в государственный кадастр недвижимости сведения о таком объекте недвижимости, которому присвоен кадастровый номер (ФЗ от 24.07.2007г. «О государственном кадастре недвижимости», статья 41).
                    </p>
                </div>
