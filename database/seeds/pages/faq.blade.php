                    Порядок согласования местоположения границ земельного участка?
                </div>
                <div class="collapsible-body grey lighten-4">
                    <p>
                        Порядок согласования местоположения границ земельных участков установлен ФЗ от 24.07.2007г. «О государственном кадастре недвижимости», статья 39. Местоположение границ земельных участков подлежит в установленном настоящим Федеральным законом порядке обязательному согласованию с заинтересованными лицами, в случае, если в результате кадастровых работ уточнено местоположение границ земельного участка, в отношении которого выполнялись соответствующие кадастровые работы, или уточнено местоположение границ смежных с ним земельных участков, сведения о которых внесены в государственный кадастр недвижимости. Согласование местоположения границ проводится с лицами, обладающими смежными земельными участками на праве:1) собственности (за исключением случаев, если такие смежные земельные участки, находящиеся в государственной или муниципальной собственности, предоставлены гражданам в пожизненное наследуемое владение, постоянное (бессрочное) пользование либо юридическим лицам, не являющимся государственными или муниципальными учреждениями либо казенными предприятиями, в постоянное (бессрочное) пользование);  2) пожизненного наследуемого владения; 3) постоянного (бессрочного) пользования (за исключением случаев, если такие смежные земельные участки предоставлены государственным или муниципальным учреждениям, казенным предприятиям, органам государственной власти или органам местного самоуправления в постоянное (бессрочное) пользование); 4) аренды (если такие смежные земельные участки находятся в государственной или муниципальной собственности и соответствующий договор аренды заключен на срок более чем пять лет).
                    </p>
