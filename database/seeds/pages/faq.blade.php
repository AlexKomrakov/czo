            <li>
                <div class="collapsible-header truncate text-italic">
                    Что какое Акт обследования?
                </div>
                <div class="collapsible-body grey lighten-4">
                    <p>
                        Акт обследования представляет собой документ, в котором кадастровый инженер в результате осмотра места нахождения здания, сооружения, помещения или объекта незавершенного строительства с учетом имеющихся кадастровых сведений о таком объекте недвижимости подтверждает прекращение существования здания, сооружения или объекта незавершенного строительства в связи с гибелью или уничтожением такого объекта недвижимости либо прекращение существования помещения в связи с гибелью или уничтожением здания или сооружения, в которых оно было расположено, гибелью или уничтожением части здания или сооружения, в пределах которой такое помещение было расположено (ФЗ от 24.07.2007г. «О государственном кадастре недвижимости», статья 42).
                    </p>
