            <li>
                <div class="collapsible-header truncate text-italic">
                    Основания для выполнения кадастровых работ?
                </div>
                <div class="collapsible-body grey lighten-4">
                    <p>
                        Кадастровые работы выполняются кадастровым инженером на основании заключаемого в соответствии с требованиями гражданского законодательства и настоящего Федерального закона договора подряда на выполнение кадастровых работ. В случаях, предусмотренных законодательством Российской Федерации, кадастровые работы могут быть выполнены кадастровым инженером на основании определения суда. Суд самостоятелен в выборе соответствующего кадастрового инженера. Расходы, связанные с выполнением таких кадастровых работ, и денежное вознаграждение соответствующему кадастровому инженеру подлежат возмещению и выплате в порядке, установленном законодательством Российской Федерации (ФЗ от 24.07.2007г. «О государственном кадастре недвижимости», статья 35).
                    </p>
                </div>
